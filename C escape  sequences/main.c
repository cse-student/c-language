#include <stdio.h>

int main() {
    //Writing on 2 line:
    printf("Hello\nWorld\n");

    //Indenting
    printf("Here we are learning how to the usage of various C escape sequences such as:\n");
    printf("\t1. Line break\n");
    printf("\t2. Indenting text\n");
    //region Printing special characters
    printf("\t3. Printing special characters such as:\n");
    printf("\t\t\\\n");
    printf("\t\t'\n");
    printf("\t\t\"\n");
    printf("\t\t\\n\n");
    printf("\t\t\\t\n");
    //endregion
    return 0;
}