#include <stdio.h>

int main() {
    printf("Printing an int: %d\n", 10);
    printf("Printing a character: %c\n", 'c');
    printf("Printing a floating point number: %f\n", 3.141592);
    printf("Printing a floating point number with specific quantity of digits after the decimal: %0.2f\n", 3.141592);
    printf("Printing a string: %s\n", "Hello World");
    printf("Printing a percentage: 10%%");

    return 0;
}